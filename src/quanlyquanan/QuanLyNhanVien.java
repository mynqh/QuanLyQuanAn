    public void LoadDaTaToTabel(){
          model.setRowCount(0);
        try {
            
             con = DatabaseHelper.getconnecDb();
             st = con.createStatement();
             System.out.println("Kết nối thành công");
             String sql = "select * from NHANVIEN";
             rs = st.executeQuery(sql);
         
                 while(rs.next()){
                Vector row = new Vector();
                row.add(rs.getString(1));
                row.add(rs.getString(2));
                row.add(rs.getString(3));
                row.add(rs.getString(4));
                row.add(rs.getDouble(5));
                row.add(rs.getString(6));
                model.addRow(row);
            }
            tblNhanVien.setModel(model);
            con.close();
         } catch (Exception e) {
             System.out.println(e);
        }
    }
    public void Display(){
        int row = tblNhanVien.getSelectedRow();
        txtMaNV.setText(tblNhanVien.getValueAt(row, 0).toString());
        txtTenNV.setText(tblNhanVien.getValueAt(row, 1).toString());
        txtNamSinh.setText(tblNhanVien.getValueAt(row, 2).toString());
        txtDiaChi.setText(tblNhanVien.getValueAt(row, 3).toString());
        txtLuong.setText(tblNhanVien.getValueAt(row, 4).toString());
        cboCa.setSelectedItem(tblNhanVien.getValueAt(row, 5).toString());
    }
    public void ThemNV(){ 
        int rs = 0;
        try {
            con = DatabaseHelper.getconnecDb();
            String sql = "insert into NHANVIEN values(?,?,?,?,?,?)";
            stmt = con.prepareStatement(sql);
            stmt.setString(1,txtMaNV.getText());
            stmt.setString(2,txtTenNV.getText());
            stmt.setString(3,txtNamSinh.getText());
            stmt.setString(4,txtDiaChi.getText());
            stmt.setDouble(5,Double.parseDouble(txtLuong.getText()));
            stmt.setString(6, (String) cboCa.getSelectedItem());
            rs = stmt.executeUpdate(); 
            if(rs > 0){
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                LoadDaTaToTabel();
            }else{
                JOptionPane.showMessageDialog(this, "Thêm không thành công");
            }
        } catch (Exception e) {
                System.out.println(e);
        }
    }
