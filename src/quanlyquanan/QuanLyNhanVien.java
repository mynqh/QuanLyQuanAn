    public boolean checkForm(){
        if(txtMaNV.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Mã nhân viên đang bỏ trống");
            return false;
        }
        if (txtTenNV.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Tên nhân viên đang bỏ trống");
            return false;
        }
        if (txtNamSinh.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Năm sinh nhân viên đang bỏ trống");
            return false;
        }
        if (txtDiaChi.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Địa chỉ đang bỏ trống");
            return false;
        }        
        if (txtLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Lương đang bỏ trống");
            return false;
        }
        try {
            Double.parseDouble(txtLuong.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lương phải là số");
            return false;
        }
        return true;
        }
